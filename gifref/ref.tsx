export const generateImage = async (prompt: string): Promise<string> => {
    if (!process.env.API_KEY) {
      console.error("API_KEY environment variable is not set.");
      throw new Error("API key is missing. Please configure your environment.");
    }
    const ai = new GoogleGenAI({ apiKey: process.env.API_KEY });
  
    try {
      const response = await ai.models.generateImages({
        model: 'imagen-4.0-generate-001',
        prompt: `A retro, pixelated, 8-bit gif style image of: ${prompt}`,
        config: {
          numberOfImages: 1,
          outputMimeType: 'image/jpeg',
          aspectRatio: '1:1',
        },
      });
  
      if (response.generatedImages && response.generatedImages.length > 0) {
        const base64ImageBytes = response.generatedImages[0].image.imageBytes;
        return base64ImageBytes;
      } else {
        throw new Error("No image was generated by the API.");
      }
    } catch (error) {
      console.error("Error generating image via Gemini API:", error);
      if (error instanceof Error) {
          return `Error: ${error.message}`;
      }
      throw new Error("Failed to generate image. The API returned an error.");
    }
  };
  