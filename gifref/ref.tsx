const GifDisplay: React.FC<GifDisplayProps> = ({ base64Image, prompt }) => {
    const imageUrl = `data:image/jpeg;base64,${base64Image}`;
  
    const handleDownload = () => {
      const link = document.createElement('a');
      link.href = imageUrl;
      const safePrompt = prompt.replace(/[^a-z0-9]/gi, '_').toLowerCase();
      link.download = `${safePrompt.slice(0, 30) || 'generated'}-image.jpeg`;
      document.body.appendChild(link);
      link.click();
      document.body.removeChild(link);
    };
  
    return (
      <div className="mt-2 p-2 border border-green-700 bg-green-900/20 rounded max-w-sm text-glow">
        <p className="mb-2 text-green-300">Generated image for: "{prompt}"</p>
        <img src={imageUrl} alt={prompt} className="w-full h-auto rounded object-cover" />
        <button
          onClick={handleDownload}
          className="mt-2 w-full bg-green-600 hover:bg-green-500 text-black font-bold py-1 px-2 rounded transition-colors duration-200"
        >
          Download Image
        </button>
      </div>
    );
  };
  
